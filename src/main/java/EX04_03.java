public class EX04_03 {
    public static void main(String[] args) {
        int a = 10;

        a++; //a=11
        System.out.printf("a++ ==> %d\n", a);

        a--; //a=10
        System.out.printf("a-- ==> %d\n", a);

        a += 5;
        System.out.printf("a+=5 ==> %d \n", a);

        a -= 5;
        System.out.printf("a-=5 ==> %d\n", a);

        a *= 5;
        System.out.printf("a*=5 ==> %d \n", a);

        a /= 5;
        System.out.printf("a/=5 ==> %d\n", a);

        a %= 5;
        System.out.printf("a %%=5 ==> %d\n", a);
    }


}
